// ── NFT receipt minting (ARC-3 compliant + immutable) ──────────────────────
//
// Strict ARC-3 requirements we now satisfy:
//   • assetURL ends with "#arc3" and points to the JSON metadata
//     (we use a `data:` URI so no IPFS pinning is required)
//   • assetMetadataHash = SHA-256 of the metadata JSON bytes
//   • All four roles (manager, reserve, freeze, clawback) are UNSET → asset
//     is permanently immutable. Even the user who minted it cannot tamper
//     with it after the txn confirms.
//
// Spec: https://arc.algorand.foundation/ARCs/arc-0003

async function sha256Bytes(bytes: Uint8Array): Promise<Uint8Array> {
  const hash = await crypto.subtle.digest("SHA-256", bytes as BufferSource)
  // Force a brand-new Uint8Array (not a typed-array view onto an ArrayBuffer)
  // because algosdk v3's strict type guards reject the latter with the
  // confusing "Argument must be byte array" error.
  const out = new Uint8Array(32)
  out.set(new Uint8Array(hash))
  return out
}

export async function mintNFTReceipt(
  algodClient: algosdk.Algodv2, senderAddress: string, vaultAppId: number,
  amount: number, recipientAddress: string,
  signTransaction: SignFn,
): Promise<{ assetId: number; txnId: string }> {
  const params = await algodClient.getTransactionParams().do()

  // ARC-3 metadata. Schema: https://arc.algorand.foundation/ARCs/arc-0003
  const metadata = {
    name: `BLISS Receipt #${vaultAppId}`,
    description: `Verifiable payment receipt: ${amount} ALGO released from escrow vault ${vaultAppId} to ${recipientAddress}.`,
    decimals: 0,
    properties: {
      app_id: vaultAppId,
      amount_algo: amount,
      recipient: recipientAddress,
      payer: senderAddress,
      timestamp: new Date().toISOString(),
      issuer: "BLISS",
    },
  }
  const metadataJson = JSON.stringify(metadata)
  const metadataBytes = new TextEncoder().encode(metadataJson)
  const metadataHash = await sha256Bytes(metadataBytes)

  // ARC-3 marker URL, must be ≤96 bytes (algosdk hard limit). The metadata
  // hash above cryptographically anchors the JSON; the URL just needs the
  // `#arc3` fragment + a stable identifier. Domain is env-driven so we never
  // bake an unowned URL into immutable on-chain receipts.
  const receiptDomain =
    (import.meta.env.VITE_RECEIPT_DOMAIN as string | undefined) || "bliss.xyz"
  const assetUrl = `https://${receiptDomain}/r/${vaultAppId}#arc3`

  const txn = algosdk.makeAssetCreateTxnWithSuggestedParamsFromObject({
    sender: senderAddress,
    suggestedParams: params,
    total: 1,
    decimals: 0,
    defaultFrozen: false,
    unitName: "RCPT",
    assetName: `Receipt-${vaultAppId}@arc3`,
    assetURL: assetUrl,
    assetMetadataHash: metadataHash,
    // CRITICAL, leave manager/reserve/freeze/clawback UNDEFINED so the
    // asset is permanently immutable.
    note: new TextEncoder().encode(metadataJson.slice(0, 1000)),
  })

  const signedTxns = await signTransaction(txn)
  const sendResponse = await algodClient.sendRawTransaction(signedTxns[0]).do()
  const txnId = extractTxId(sendResponse)
  const result = await algosdk.waitForConfirmation(algodClient, txnId, 4) as any
  // algosdk v3 returns assetIndex as a bigint at multiple possible paths
  const rawId =
    result?.assetIndex ??
    result?.["asset-index"] ??
    result?.applicationIndex ??
    result?.["application-index"] ??
    result?.["created-asset-index"] ??
    0
  const assetId = typeof rawId === "bigint" ? Number(rawId) : Number(rawId)
  if (!assetId) {
    throw new Error(
      `NFT receipt minted but assetId could not be parsed from confirmation. txnId=${txnId}. ` +
      `Inspect the txn on Lora to retrieve the asset id manually.`
    )
  }
  return { assetId, txnId }
}

// ── Service Registry write ─────────────────────────────────────────────────
//
// ARC-4 ABI-method call into the on-chain ServiceRegistry contract. We use
// algosdk's ABIMethod here (instead of a hand-rolled selector + manual encode)
// because the method takes string args of variable length, ABIMethod handles
// the length-prefixing for us, matching exactly what puyapy compiled.
//
// The contract enforces "only original provider can update", no anti-spam
// fee is checked on-chain in the live App ID, so we keep the call minimal.
const REGISTER_METHOD = new algosdk.ABIMethod({
  name: "register",
  args: [
    { type: "string", name: "service_id" },
    { type: "uint64", name: "price_microalgos" },
    { type: "uint64", name: "cycle_days" },
    { type: "string", name: "name" },
  ],
  returns: { type: "void" },
})

// Build the box name the contract uses to store this listing.
// puyapy BoxMap with key_prefix="svc:" stores as `b"svc:" + ABI(arc4.String)(key)`.
// ABI string encoding = uint16 BE length prefix + UTF-8 bytes.
function buildServiceBoxName(serviceId: string): Uint8Array {
  const idBytes = new TextEncoder().encode(serviceId)
  const out = new Uint8Array(4 + 2 + idBytes.length)
  out.set([0x73, 0x76, 0x63, 0x3a], 0) // "svc:"
  out[4] = (idBytes.length >> 8) & 0xff
  out[5] = idBytes.length & 0xff
  out.set(idBytes, 6)
  return out
}

export async function registerService(
  algodClient: algosdk.Algodv2,
  senderAddress: string,
  registryAppId: number,
  service: { service_id: string; price_microalgos: number; cycle_days: number; name: string },
  signTransaction: SignFn,
): Promise<string> {
  const params = await algodClient.getTransactionParams().do()
  // Box-storage requires explicit box references on the app call. Otherwise
  // the AVM rejects the access with "invalid Box reference".
  const boxName = buildServiceBoxName(service.service_id)
  const atc = new algosdk.AtomicTransactionComposer()
  atc.addMethodCall({
    appID: registryAppId,
    method: REGISTER_METHOD,
    methodArgs: [
      service.service_id,
      BigInt(Math.max(0, Math.floor(service.price_microalgos))),
      BigInt(Math.max(1, Math.floor(service.cycle_days))),
      service.name,
    ],
    sender: senderAddress,
    suggestedParams: params,
    boxes: [{ appIndex: registryAppId, name: boxName }],
    signer: async (txnGroup, indexesToSign) => {
      // Bridge our SignFn (single-txn signer) into AtomicTransactionComposer's
      // signer interface, which expects an array of signed bytes.
      const out: Uint8Array[] = []
      for (const i of indexesToSign) {
        const signed = await signTransaction(txnGroup[i])
        out.push(signed[0])
      }
      return out
    },
  })
  const result = await atc.execute(algodClient, 4)
  return result.txIDs[0]
}

