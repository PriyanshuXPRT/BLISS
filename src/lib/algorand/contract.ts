/**
 * Send ALGO to the escrow vault's application address.
 * The extra 100_000 µALGO covers the minimum balance requirement.
 */
export async function fundEscrowContract(
  algodClient: algosdk.Algodv2, senderAddress: string, appAddress: string,
  amountMicroAlgos: number, signTransaction: SignFn,
): Promise<string> {
  const params = await algodClient.getTransactionParams().do()
  const txn = algosdk.makePaymentTxnWithSuggestedParamsFromObject({
    sender: senderAddress, receiver: appAddress,
    amount: amountMicroAlgos + 100_000,
    suggestedParams: params,
  })
  const signedTxns = await signTransaction(txn)
  const sendResponse = await algodClient.sendRawTransaction(signedTxns[0]).do()
  const txnId = extractTxId(sendResponse)
  await algosdk.waitForConfirmation(algodClient, txnId, 4)
  return txnId
}

// ── ARC-4 NoOp method calls ────────────────────────────────────────────────

/**
 * Call a NoOp method on an application using the correct ARC-4 selector.
 * extraFee=true sets fee = 2x the network minimum to cover one inner transaction
 * (1x outer + 1x inner). The minimum is read from suggestedParams so we
 * automatically track any future on-chain fee changes instead of hard-coding 1000.
 */
async function callMethod(
  algodClient: algosdk.Algodv2, sender: string, appId: number,
  selector: Uint8Array, signTransaction: SignFn,
  extraFee = false,
): Promise<string> {
  const params = await algodClient.getTransactionParams().do()
  const minFee = Number(params.minFee ?? params.fee ?? 1000) || 1000
  const txn = algosdk.makeApplicationCallTxnFromObject({
    sender, appIndex: appId,
    onComplete: algosdk.OnApplicationComplete.NoOpOC,
    suggestedParams: extraFee ? { ...params, fee: minFee * 2, flatFee: true } : params,
    appArgs: [selector],
  })
  const signedTxns = await signTransaction(txn)
  const sendResponse = await algodClient.sendRawTransaction(signedTxns[0]).do()
  const txnId = extractTxId(sendResponse)
  await algosdk.waitForConfirmation(algodClient, txnId, 4)
  return txnId
}

export async function releaseEscrowFunds(
  algodClient: algosdk.Algodv2, senderAddress: string, appId: number,
  signTransaction: SignFn,
): Promise<string> {
  return callMethod(algodClient, senderAddress, appId, SEL.release, signTransaction, true)
}

/** ARC-4 selector for AgentEscrowVaultV2.release(uint64)uint64. */
const SEL_RELEASE_V2 = new Uint8Array([0x61, 0x17, 0xcc, 0xb8])

/**
 * Build the box name the AgentEscrowVaultV2 contract writes billing records to.
 * Contract stores: BoxMap key = "h:" + arc4.UInt64(new_index)
 * new_index = current cycle_index + 1
 */
function buildBillingBoxName(nextCycleIndex: number): Uint8Array {
  const prefix = new TextEncoder().encode("h:")
  const indexBytes = algosdk.encodeUint64(nextCycleIndex)
  const name = new Uint8Array(prefix.length + indexBytes.length)
  name.set(prefix, 0)
  name.set(indexBytes, prefix.length)
  return name
}

/**
 * Release funds from an Agent Escrow Vault v2.
 *
 * Sends two transactions as an atomic group signed together in ONE Pera popup:
 *   1. MBR payment to fund the new billing-record box (16100 µALGO)
 *   2. The release(uint64) app call with the correct box reference
 *
 * Both txns are grouped and signed in a single Pera Wallet prompt to avoid
 * the 4100 "pending transaction" error that occurs when signing sequentially.
 */
export async function releaseAgentVaultV2(
  algodClient: algosdk.Algodv2, senderAddress: string, appId: number,
  amountMicroAlgos: number, signTransaction: SignFn,
  proof?: string,
): Promise<string> {
  const params = await algodClient.getTransactionParams().do()
  const minFee = Number(params.minFee ?? params.fee ?? 1000) || 1000

  // Read current cycle_index from global state to compute the correct box name
  let nextCycleIndex = 1
  try {
    const appInfo = await algodClient.getApplicationByID(appId).do() as any
    const globalState: Array<{ key: string; value: { type: number; uint: number } }> =
      appInfo?.params?.["global-state"] ?? appInfo?.params?.globalState ?? []
    const cycleEntry = globalState.find((e) => {
      try {
        // algosdk v3 may return key as base64 string or already decoded
        const decoded = typeof e.key === "string" ? atob(e.key) : new TextDecoder().decode(e.key as any)
        return decoded === "cycle_index"
      } catch { return false }
    })
    if (cycleEntry) nextCycleIndex = (cycleEntry.value?.uint ?? 0) + 1
  } catch {
    // Default to 1 — first release
  }

  const boxName = buildBillingBoxName(nextCycleIndex)
  const appAddress = algosdk.getApplicationAddress(appId)

  // Box MBR: 2500 (base) + 400 * (key_len + value_len)
  // key = "h:" (2 bytes) + uint64 (8 bytes) = 10 bytes; value = BillingRecord = 24 bytes
  const BOX_MBR = 2500 + 400 * (10 + 24) // 16100 µALGO

  const noteBytes = proof ? clampNoteBytes(`ub:proof-of-delivery:${proof}`, 900) : undefined

  // Build both txns
  const mbrTxn = algosdk.makePaymentTxnWithSuggestedParamsFromObject({
    sender: senderAddress,
    receiver: appAddress,
    amount: BOX_MBR,
    suggestedParams: { ...params, fee: minFee, flatFee: true },
  })
  const releaseTxn = algosdk.makeApplicationCallTxnFromObject({
    sender: senderAddress,
    appIndex: appId,
    onComplete: algosdk.OnApplicationComplete.NoOpOC,
    suggestedParams: { ...params, fee: minFee * 2, flatFee: true },
    appArgs: [SEL_RELEASE_V2, algosdk.encodeUint64(amountMicroAlgos)],
    boxes: [{ appIndex: appId, name: boxName }],
    note: noteBytes,
  })

  // Assign group ID so both txns are atomic
  algosdk.assignGroupID([mbrTxn, releaseTxn])

  // Sign BOTH txns in a single Pera Wallet popup (avoids 4100 error)
  const signedGroup = await signTransaction([mbrTxn, releaseTxn] as any)

  // Send the group
  const sendResponse = await algodClient.sendRawTransaction(signedGroup).do()
  const txnId = extractTxId(sendResponse)
  await algosdk.waitForConfirmation(algodClient, txnId, 4)
  return txnId
}

/**
 * Release vault funds and attach a free-form proof-of-delivery note to the
 * application call transaction. The note is stored permanently in the
 * transaction record on Algorand and can be retrieved by anyone, making
 * it a tamper-proof receipt that the merchant delivered the service.
 *
 * Used for v1-style vaults (DisputeEscrow, EscrowVault, MultiSigEscrow,
 * TimeLockEscrow, ASAEscrow) whose `release()void` selector takes no args.
 * For Agent Escrow Vault v2, call `releaseAgentVaultV2` instead.
 */
export async function releaseEscrowFundsWithProof(
  algodClient: algosdk.Algodv2, senderAddress: string, appId: number,
  proof: string, signTransaction: SignFn,
): Promise<string> {
  const params = await algodClient.getTransactionParams().do()
  const minFee = Number(params.minFee ?? params.fee ?? 1000) || 1000
  const note = clampNoteBytes(`ub:proof-of-delivery:${proof}`, 900)
  const txn = algosdk.makeApplicationCallTxnFromObject({
    sender: senderAddress, appIndex: appId,
    onComplete: algosdk.OnApplicationComplete.NoOpOC,
    suggestedParams: { ...params, fee: minFee * 2, flatFee: true },
    appArgs: [SEL.release],
    note,
  })
  const signedTxns = await signTransaction(txn)
  const sendResponse = await algodClient.sendRawTransaction(signedTxns[0]).do()
  const txnId = extractTxId(sendResponse)
  await algosdk.waitForConfirmation(algodClient, txnId, 4)
  return txnId
}

/**
 * Encode a note string and trim it to fit within `maxBytes` UTF-8 bytes
 * without breaking a multi-byte codepoint. Algorand's per-txn note ceiling
 * is 1024 bytes; we keep a small safety margin.
 */
function clampNoteBytes(s: string, maxBytes: number): Uint8Array {
  const enc = new TextEncoder()
  let bytes = enc.encode(s)
  if (bytes.length <= maxBytes) return bytes
  // Walk back to a safe codepoint boundary.
  let str = s
  while (bytes.length > maxBytes) {
    str = str.slice(0, -1)
    bytes = enc.encode(str)
  }
  return bytes
}

// ── Box Storage: AgentEscrowVaultV2 billing history ────────────────────────

export interface BillingRecord {
  cycle: number
  timestamp: number  // unix seconds
  amount: number     // microAlgos
}

/**
 * Read all on-chain BillingRecords from an AgentEscrowVaultV2 contract.
 * Each release writes a box keyed by "h:<cycle>" (uint64 big-endian) holding
 * a 24-byte struct (timestamp:uint64, amount:uint64, cycle_index:uint64).
 *
 * Returns the records sorted oldest → newest. Falls back to an empty array
 * if the application has no boxes (e.g. legacy v1 vault).
 */
export async function getVaultBillingHistory(
  algodClient: algosdk.Algodv2, appId: number,
): Promise<BillingRecord[]> {
  let resp: any
  try {
    resp = await algodClient.getApplicationBoxes(appId).do()
  } catch {
    return []
  }
  const boxes: Array<{ name: Uint8Array }> = resp?.boxes ?? []
  const out: BillingRecord[] = []
  for (const b of boxes) {
    const name = b.name instanceof Uint8Array ? b.name : new Uint8Array(b.name)
    // We only care about boxes prefixed with "h:" (0x68, 0x3a)
    if (name.length < 3 || name[0] !== 0x68 || name[1] !== 0x3a) continue
    try {
      const value = await algodClient.getApplicationBoxByName(appId, name).do()
      const v: Uint8Array = value?.value instanceof Uint8Array ? value.value : new Uint8Array(value?.value ?? [])
      if (v.length < 24) continue
      const timestamp = Number(algosdk.decodeUint64(v.slice(0, 8), "safe"))
      const amount = Number(algosdk.decodeUint64(v.slice(8, 16), "safe"))
      const cycle = Number(algosdk.decodeUint64(v.slice(16, 24), "safe"))
      out.push({ cycle, timestamp, amount })
    } catch {
      // Skip unreadable box but keep going.
    }
  }
  out.sort((a, b) => a.cycle - b.cycle)
  return out
}

