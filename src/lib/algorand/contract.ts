// ── ARC-4 method selectors (extracted from compiled TEAL) ──────────────────
//
// Each selector = first 4 bytes of sha512_256(method_signature).
// These are deterministic, generated by puyapy from the Python source.
//
const SEL = {
  // Shared across all vault types
  delete:  new Uint8Array([0x24, 0x37, 0x8d, 0x3c]), // delete()void
  kill:    new Uint8Array([0xb9, 0xc2, 0x11, 0x55]), // kill()void
  release: new Uint8Array([0x07, 0x6b, 0xbd, 0x4d]), // release()void

  // Per-vault create selectors (differ by signature)
  createAddr:       new Uint8Array([0xcc, 0x69, 0x4e, 0xaa]), // create(address)void        , EscrowVault
  createAddrUint64: new Uint8Array([0xc0, 0xb6, 0x43, 0x52]), // create(address,uint64)void  , TimeLockEscrow, ASAEscrow
  createAddrAddr:   new Uint8Array([0x8a, 0x96, 0x98, 0x0e]), // create(address,address)void , MultiSigEscrow, DisputeEscrow

  // Method-specific
  approve: new Uint8Array([0xd6, 0x11, 0xdb, 0xd8]), // approve()void , MultiSigEscrow
  optin:   new Uint8Array([0xdc, 0x0d, 0xe7, 0xeb]), // optin()void   , ASAEscrow
}

// ── Artifact loader ────────────────────────────────────────────────────────

type Arc56Spec = {
  byteCode: { approval: string; clear: string }
  state: { schema: { global: { ints: number; bytes: number }; local: { ints: number; bytes: number } } }
}

function b64ToBytes(b64: string): Uint8Array {
  return new Uint8Array(atob(b64).split("").map((c) => c.charCodeAt(0)))
}

interface ArtifactData {
  approvalBytes: Uint8Array
  clearBytes: Uint8Array
  numGlobalInts: number
  numGlobalByteSlices: number
  numLocalInts: number
  numLocalByteSlices: number
}

function loadArtifact(spec: Arc56Spec): ArtifactData {
  return {
    approvalBytes: b64ToBytes(spec.byteCode.approval),
    clearBytes: b64ToBytes(spec.byteCode.clear),
    numGlobalInts: spec.state.schema.global.ints,
    numGlobalByteSlices: spec.state.schema.global.bytes,
    numLocalInts: spec.state.schema.local.ints,
    numLocalByteSlices: spec.state.schema.local.bytes,
  }
}

function artifactForType(type: VaultType): ArtifactData {
  const map: Record<VaultType, Arc56Spec> = {
    standard:    EscrowVaultSpec as Arc56Spec,
    agent:       AgentEscrowVaultSpec as Arc56Spec,
    agent_v2:    AgentEscrowVaultV2Spec as Arc56Spec,
    time_locked: TimeLockSpec as Arc56Spec,
    multi_sig:   MultiSigSpec as Arc56Spec,
    dispute:     DisputeSpec as Arc56Spec,
    asa:         ASASpec as Arc56Spec,
  }
  return loadArtifact(map[type])
}

// ── Helpers ────────────────────────────────────────────────────────────────

function extractTxId(response: unknown): string {
  if (typeof response === "string") return response
  const r = response as Record<string, unknown>
  return String(r?.txid ?? r?.txId ?? r?.["txId"] ?? "")
}

function addrBytes(address: string): Uint8Array {
  return algosdk.decodeAddress(address).publicKey
}

type SignFn = (txn: algosdk.Transaction | algosdk.Transaction[]) => Promise<Uint8Array[]>

// ── Core deployment ────────────────────────────────────────────────────────

interface DeployResult { appId: number; appAddress: string; txnId: string }

async function deployApp(
  algodClient: algosdk.Algodv2,
  sender: string,
  artifact: ArtifactData,
  appArgs: Uint8Array[],
  signTransaction: SignFn,
): Promise<DeployResult> {
  const params = await algodClient.getTransactionParams().do()

  const txn = algosdk.makeApplicationCreateTxnFromObject({
    sender,
    suggestedParams: params,
    onComplete: algosdk.OnApplicationComplete.NoOpOC,
    approvalProgram: artifact.approvalBytes,
    clearProgram: artifact.clearBytes,
    numGlobalInts: artifact.numGlobalInts,
    numGlobalByteSlices: artifact.numGlobalByteSlices,
    numLocalInts: artifact.numLocalInts,
    numLocalByteSlices: artifact.numLocalByteSlices,
    appArgs,
  })

  const signedTxns = await signTransaction(txn)
  const sendResponse = await algodClient.sendRawTransaction(signedTxns[0]).do()
  const txnId = extractTxId(sendResponse)
  const result = await algosdk.waitForConfirmation(algodClient, txnId, 4)
  const appId = Number(
    (result as any).applicationIndex ?? (result as any)["application-index"] ?? 0
  )
  if (appId === 0) throw new Error("Failed to retrieve application ID after deployment")
  const appAddress = String(algosdk.getApplicationAddress(appId))

  // Small delay to let WalletConnect close the first signing session before
  // the next signing request (fund txn). Without this Pera throws 4100.
  await new Promise((r) => setTimeout(r, 800))

  return { appId, appAddress, txnId }
}

